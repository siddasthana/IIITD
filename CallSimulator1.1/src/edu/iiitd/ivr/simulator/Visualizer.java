    /**
     * Updates the ordering of startCall in List_call after change in startCall order
     * performed by clicking randomize or manual button.
     */
    public void repaint_List_Call() {
        dlm.removeAllElements();
        dlm.ensureCapacity(0x4b0);
        System.out.println("Size of loaded list :"+simulatorController.callList.size());
        for (Call e : simulatorController.callList) {
            dlm.addElement(e);
        }
        Lst_Call.setModel(dlm);
        Lst_Call.setSelectedIndex(0);
        Lst_Call.updateUI();
        ScrlPan_Call.updateUI();
//        Pnl_Call.updateUI();
        this.update(this.getGraphics());
    }
