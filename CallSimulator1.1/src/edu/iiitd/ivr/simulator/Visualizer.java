    private void Btn_StartSimulationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_StartSimulationActionPerformed
        // TODO add your handling code here:
       int lines = Integer.parseInt(Txt_Line.getText());
        if(lines==1){
        simulatorController.start();
        StatusMonitor Cl = new StatusMonitor();
        Cl.start();
       }else{
        //List<SimulatorController> ControllerList = new ArrayList();
        LogicProcessor[] sm = new LogicProcessor[lines];
        int temp=0;
        for(Call cl : simulatorController.callList){
            sm[temp%lines].callList.add(cl);
        temp++;
        }
        
        for(LogicProcessor smObj : sm){
        //Thread st = new Thread(smObj);
        //st.start();
            smObj.start();
        }
        StatusMonitor Cl = new StatusMonitor();
        Cl.start();
        }
        Timer timer = new Timer();
        timer.scheduleAtFixedRate(new Simulationupdate(),2000, 2000);
        
    }//GEN-LAST:event_Btn_StartSimulationActionPerformed

    private void jButtonUPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUPActionPerformed
        // TODO add your handling code here:
int index = Lst_Call.getSelectedIndex();
        if( index == -1 ){
        //  JOptionPane.showMessageDialog( null, "Select something to move." );
        }else if( index > 0 )
        {
            Call removed = simulatorController.callList.remove(index);
         // String temp = (String)nameModel.remove( index );
          simulatorController.callList.add( index - 1, removed );
          repaint_List_Call();
          Lst_Call.setSelectedIndex( index - 1 );
        }

    }//GEN-LAST:event_jButtonUPActionPerformed

    private void jButtonDownActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDownActionPerformed
        // TODO add your handling code here:
        int index = Lst_Call.getSelectedIndex();
if( index == -1 ){
        //  JOptionPane.showMessageDialog( null, "Select something to move." );
        }else if( index < simulatorController.callList.size()-1 )
        {
            Call removed = simulatorController.callList.remove(index);
          simulatorController.callList.add( index + 1, removed );
          repaint_List_Call();
          Lst_Call.setSelectedIndex( index + 1 );
        }
    }//GEN-LAST:event_jButtonDownActionPerformed

